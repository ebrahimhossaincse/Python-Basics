'''
Membership Operators Examples
'''

# in Operator
my_list = [1, 2, 3, 4, 5]

print("in Operator:")
print(3 in my_list)  # Output: True, because 3 is in the list
print(6 in my_list)  # Output: False, because 6 is not in the list

# not in Operator
print("not in Operator:")
print(3 not in my_list)  # Output: False, because 3 is in the list
print(6 not in my_list)  # Output: True, because 6 is not in the list
