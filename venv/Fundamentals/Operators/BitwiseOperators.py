'''
Bitwise Operators Examples
'''

# Bitwise AND (&)
x = 5
y = 3
result = x & y
print("Bitwise AND (&):", result)

# Bitwise OR (|)
x = 5
y = 3
result = x | y
print("Bitwise OR (|):", result)

# Bitwise XOR (^)
x = 5
y = 3
result = x ^ y
print("Bitwise XOR (^):", result)

# Bitwise NOT (~)
x = 5
result = ~x
print("Bitwise NOT (~):", result)

# Bitwise Left Shift (<<)
x = 5
result = x << 1
print("Bitwise Left Shift (<<):", result)

# Bitwise Right Shift (>>)
x = 5
result = x >> 1
print("Bitwise Right Shift (>>):", result)
