'''
Logical Operators Examples
'''

# AND Operator (and)
x = True
y = False
result = x and y
print("AND Operator (and):", result)

# OR Operator (or)
x = True
y = False
result = x or y
print("OR Operator (or):", result)

# NOT Operator (not)
x = True
result = not x
print("NOT Operator (not):", result)