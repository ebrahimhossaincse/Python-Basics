# Convert an integer to a boolean
x = 0
bool_x = bool(x)
print(bool_x)  # Output: False

# Convert a non-empty string to a boolean
str_value = "Hello"
bool_str = bool(str_value)
print(bool_str)  # Output: True

# Convert an empty string to a boolean
empty_str = ""
bool_empty = bool(empty_str)
print(bool_empty)  # Output: False
