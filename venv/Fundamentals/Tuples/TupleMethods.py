# Define a tuple
my_tuple = (1, 2, 3, 4, 5)

# Count method - counts the number of occurrences of a value
count_of_3 = my_tuple.count(3)
print("Count of 3:", count_of_3)

# Index method - returns the index of the first occurrence of a value
index_of_4 = my_tuple.index(4)
print("Index of 4:", index_of_4)

# Length of a tuple
length_of_tuple = len(my_tuple)
print("Length of tuple:", length_of_tuple)

# Minimum and maximum values in a tuple
min_value = min(my_tuple)
max_value = max(my_tuple)
print("Minimum value:", min_value)
print("Maximum value:", max_value)

# Converting tuple to a list
tuple_to_list = list(my_tuple)
print("Tuple converted to list:", tuple_to_list)
