# Define an empty set
my_set = set()

# Adding single items to the set using add() method
my_set.add("apple")
my_set.add("banana")
my_set.add("cherry")

# Print the set after adding items
print("Set after adding single items:", my_set)

# Adding multiple items to the set using update() method
my_set.update(["orange", "kiwi", "grape"])

# Print the set after adding multiple items
print("Set after adding multiple items:", my_set)
