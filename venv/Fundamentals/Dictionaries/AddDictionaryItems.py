# Define an empty dictionary
my_dict = {}

# Adding single items to the dictionary using assignment
my_dict["brand"] = "Ford"
my_dict["model"] = "Mustang"
my_dict["year"] = 1964

# Print the dictionary after adding items
print("Dictionary after adding single items:", my_dict)

# Adding multiple items to the dictionary using update() method
my_dict.update({"color": "red", "engine": "V8"})

# Print the dictionary after adding multiple items
print("Dictionary after adding multiple items:", my_dict)
